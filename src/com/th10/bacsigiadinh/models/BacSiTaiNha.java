package com.th10.bacsigiadinh.models;

public class BacSiTaiNha {
	private String id;
	private String ten_quan;
	private String dia_chi;
	private String so_dt;
	private String time;
	private String id_user;
	
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTen_quan() {
		return ten_quan;
	}
	public void setTen_quan(String ten_quan) {
		this.ten_quan = ten_quan;
	}
	public String getDia_chi() {
		return dia_chi;
	}
	public void setDia_chi(String dia_chi) {
		this.dia_chi = dia_chi;
	}
	public String getSo_dt() {
		return so_dt;
	}
	public void setSo_dt(String so_dt) {
		this.so_dt = so_dt;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getId_user() {
		return id_user;
	}
	public void setId_user(String id_user) {
		this.id_user = id_user;
	}
	
	

}
